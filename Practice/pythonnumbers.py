# python numbers - integer
int1 = -2989
int2 = 0
int3 = 334
int4 = +33
print(int1, type(int1)) # -2989 <class 'int'>
print(int2, type(int2)) # 0 <class 'int'>
print(int3, type(int3)) # 334 <class 'int'>
print(int4, type(int4)) # 33 <class 'int'>

#python numbers - float
flt1 = -2.5533
flt2 = 0.122
flt3 = 2.444
flt4 = 2.44e34
flt5 = -4.63e44
print(flt1, type(flt1)) # -2.5533 <class 'float'>
print(flt2, type(flt2)) # 0.122 <class 'float'>
print(flt3, type(flt3)) # 2.444 <class 'float'>
print(flt4, type(flt4)) # 2.44e+34 <class 'float'>
print(flt5, type(flt5)) # -4.63e+44 <class 'float'>

# python numbers - complex
cmplx1 = 2+4j
cmplx2 =-(4+9j)
cmplx3 = -4.1j
cmplx4 = 6j
print(cmplx1, type(cmplx1)) # (2+4j) <class 'complex'>
print(cmplx2, type(cmplx2)) # (-4-9j) <class 'complex'>
print(cmplx3, type(cmplx3)) # (-0-4.1j) <class 'complex'>
print(cmplx4, type(cmplx4)) # 6j <class 'complex'>