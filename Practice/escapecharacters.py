# escape characters
str1 = "He is very \"Genius\"."
str2 = 'He is very \'Genius\'.'
str3 = "He\'s is very genius"
print(str1, type(str1), id(str1))
print(str2, type(str2), id(str2))
print(str3, type(str3), id(str3))

# escape characters - newline (\n)
str4 ="I love to programming. \nI enjoy pto code in python as it is simple and easy. \nLet's learn it together"
print(str4)

# escape characters - tab (\t)
str5 = "Hello \t\t\tWorld \t!!!"
print(str5)

# escape characters - backspace (\b)
str6 = "Hello  \bWorld"
print(str6)

# escape characters - backslash (\\)
str7 = "What do you like to eat? \"Apple\\Orange\""
print(str7)