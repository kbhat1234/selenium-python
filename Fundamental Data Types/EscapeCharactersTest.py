# no escape characters
print('durgasoftware')
'''
durgasoftware
'''

# \t escape character (horizontal tab)
print('durga\tsoftware')
'''
durga   software
'''

# \b escape character (backspace)
print('durga \bsoftware')
'''
durgasoftware
'''

# \n escape character (new line)
print('durga\nsoftware\nsolutions\tpvt limited')
'''
durga
software
solutions   pvt limited
'''

# \r escape character (carriage return)
print('durga\rsoftware')
'''
software
'''

# \' escape character (single quote)
print('hello all, everyone\'s fine')
'''
hello all, everyone's fine
'''

# \" escape character (double quote)
print('Hello, Welcome to \"Amazon\" world')
'''
Hello, Welcome to "Amazon" world
'''

# \\ escape character (backslash character)
print('C:\\curl\\bin')
'''
C:\curl\bin
'''

