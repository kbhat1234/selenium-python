# membership operators (in, not in)
a = [10, 20, 30, 40]

print(40 in a)
print(33 in a)
print(88 not in a)
print(20 not in a)

str1 = "Welcome to python programming!"
print('w' in str1)
print('W' in str1)
print('python' in str1)
print('Python' in str1)
print('!' in str1)

print('python' not in str1)
print('Python' not in str1)
print(' ' in str1)
